from fastapi import status, HTTPException
import time

## Internal Packages
from config.logger_config import get_logger
from database.db_queries import execute_ai_generated_sql, clean_sql_query

## Initiate Logger
logging = get_logger(__name__)

async def sql_agent(start_time, sql_generation_prompt, client_dbconnection_pool, text_generation_model, span):
    
    ## Give prompt to the LLM to generate SQL
    sql = text_generation_model.generate_response(sql_generation_prompt)

    span.set_attributes({
                "llm.output_messages.0.message.role": "assistant",
                "llm.output_messages.0.message.content":  str(sql),
            })

    if not sql or not isinstance(sql, str):
        error_msg = f"SQL not generated by language model. AI Output: {sql}"
        logging.error(error_msg)
        
        process_time = time.time() - start_time
        
        # Raise exception that FastAPI will convert into a response
        span.record_exception("SQL not generated by language model.")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="SQL not generated by language model.",
            headers={"X-Response-Time": f"{process_time:.6f} seconds"}
        )
    
    sql = clean_sql_query(sql)

    if sql == "SELECT 'User request cannot be fulfilled.';" or sql == "SELECT 'User request cannot be fulfilled.'":
        logging.info("User request cannot be fulfilled. SQL: %s", sql)

        process_time = time.time() - start_time
        
        # Raise exception that FastAPI will convert into a response
        span.record_exception("LLM Generated: SELECT 'User request cannot be fulfilled.'")
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="User request cannot be fulfilled.",
            headers={"X-Response-Time": f"{process_time:.6f} seconds"}
        )
    
    return await execute_ai_generated_sql(sql, client_dbconnection_pool), sql