from fastapi import status, HTTPException
import time
import asyncio
import functools

## Internal Packages
from config import get_logger
from database import execute_ai_generated_sql, format_sql_query

## Initiate Logger
logging = get_logger(__name__)

async def sql_agent(start_time, sql_generation_prompt, client_dbconnection_pool, text_generation_model, span, event_loop, trace, facm_code):
    
    ## Give prompt to the LLM to generate SQL
    # sql = text_generation_model.generate_response(sql_generation_prompt)
    
    def _sql_generation(sql_generation_prompt, span):
        with trace.use_span(span):
            return text_generation_model.generate_response(sql_generation_prompt)

    sql_result = await asyncio.gather(event_loop.run_in_executor(None, 
                        functools.partial(_sql_generation, sql_generation_prompt, span)
                        ))
    
    raw_sql = sql_result[0]

    span.set_attributes({
                "llm.output_messages.0.message.role": "assistant",
                "llm.output_messages.0.message.content":  str(raw_sql),
            })
    
    if not raw_sql or not isinstance(raw_sql, str):
        error_msg = f"SQL not generated by language model. AI Output: {raw_sql}"
        logging.error(error_msg)
        
        process_time = time.time() - start_time
        
        # Raise exception that FastAPI will convert into a response
        span.record_exception(Exception("SQL not generated by language model."))
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="SQL not generated by language model.",
            headers={"X-Response-Time": f"{process_time:.6f} seconds"}
        )
    
    sql = format_sql_query(raw_sql, facm_code)
    if sql == "SELECT 'User request cannot be fulfilled.';" or sql == "SELECT 'User request cannot be fulfilled.'":
        logging.info("User request cannot be fulfilled. SQL: %s", sql)

        process_time = time.time() - start_time
        
        # Raise exception that FastAPI will convert into a response
        span.record_exception(Exception("LLM Generated: SELECT 'User request cannot be fulfilled.'"))
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="User request cannot be fulfilled.",
            headers={"X-Response-Time": f"{process_time:.6f} seconds"}
        )
    
    return await execute_ai_generated_sql(sql, client_dbconnection_pool)